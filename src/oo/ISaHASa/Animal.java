package oo.ISaHASa;

public class Animal {

    private int weigth;
    private int hight;

    public Animal(int weigth, int hight) {
        this.weigth = weigth;
        this.hight = hight;
    }

    public int getWeigth() {
        return weigth;
    }

    public void setWeigth(int weigth) {
        this.weigth = weigth;
    }

    public int getHight() {
        return hight;
    }

    public void setHight(int hight) {
        this.hight = hight;
    }
}

